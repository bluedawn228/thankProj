package com.mulcam.project.vo;

public class PharseVO {
	
	private int phrsId;
	private String phrsText;
	private String author;
	
	public int getPhrsId() {
		return phrsId;
	}
	public void setPhrsId(int phrsId) {
		this.phrsId = phrsId;
	}
	public String getPhrsText() {
		return phrsText;
	}
	public void setPhrsText(String phrsText) {
		this.phrsText = phrsText;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	
	

}
